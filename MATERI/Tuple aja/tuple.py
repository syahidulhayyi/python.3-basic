print("---------TUPLE---------------")
#Tuple () () berurutan, tidak bisa diubah, boleh duplikat )
Tanggal_lahir = ("09", "Mei", "2008")
print("tanggal lahir bang jepri:",Tanggal_lahir)
print("tanggal lahir bang jepri:",Tanggal_lahir[0])  # akses index ke-0
print("bulan lahir bang jepri:",Tanggal_lahir[1])  # akses index ke-1
print("tahun lahir bang jepri:",Tanggal_lahir[2])  # akses index ke-2

#akses bulan (posisi index) 
print("bulan tahun:", Tanggal_lahir[2:4])  # akses index ke-2 sampai ke-4
